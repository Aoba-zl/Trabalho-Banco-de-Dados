package model;

public class Imagem
{
    private byte bytesImagem;
    private String nome;

    public byte getBytesImagem()
	{
        return bytesImagem;
    }

    public void setBytesImagem(byte bytesImagem)
	{
        this.bytesImagem = bytesImagem;
    }

    public String getNome()
	{
        return nome;
    }

    public void setNome(String nome)
	{
        this.nome = nome;
    }
}
